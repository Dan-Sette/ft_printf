#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*aux;

	aux = lst;
	while (aux)
	{
		if (aux->next == 0)
			return (aux);
		aux = aux->next;
	}
	return (NULL);
}
