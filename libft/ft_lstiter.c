#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*aux;

	aux = lst;
	if (!lst)
		return ;
	while (aux != NULL)
	{
		f(aux->content);
		aux = aux->next;
	}
}
